package com.SiddhantBhatia.DAY_39;
public class HighLow {
        public static void main(String[] args) {
            int i=1;
            while (i <= 10) {
                if (i <= 5) {
                    System.out.println("Low");
                } else {
                    System.out.println("High");
                }
                i++;
            }
        }
    }